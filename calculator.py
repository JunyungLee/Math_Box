
def add(a, b):
	return a+b
def subtract(a, b):
	return a-b
def multiply(a, b):
	return a*b


def getMedian(a, b):
    return (a+b)/2

def divide_new(a, b):
	return a/b

def getRemainder(a, b):
    return a//b

def get_Abs(num):
	if num>=0:
		return num
	else: 
		return -num

def get_percent(a, b):
	return (a/b) * 100

